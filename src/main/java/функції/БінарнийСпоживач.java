package функції;

/**
 * Локалізований аналог {@link java.util.function.BiConsumer}
 */
@FunctionalInterface
public interface БінарнийСпоживач<A, B> {
    /**
     * Виконує цю операцію над даними двома елементами
     *
     * @param першийЕлемент перший вхідний елемент
     * @param другийЕлемент другий вхідний елмент
     */
    void прийняти(A першийЕлемент, B другийЕлемент);
}
