package презентація.п4;

/**
 * Остання ланка потоку, яка закінчує його, повертаючи результат виконання усього потоку.
 */
public interface ТермінальнийОператор<R> {
    /**
     * Закінчує потік, виконуючи всі проміжні операції і повертаючи результат його виконання
     *
     * @return результат потоку
     */
    R закінчити();
}
